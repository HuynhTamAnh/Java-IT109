//Khai báo các biến cơ bản:
//Khai báo biến a có kiểu int để lưu trữ một số nguyên.
//Khai báo biến db có kiểu double để lưu trữ một số thực.
//Khai báo biến ch có kiểu char để lưu trữ một ký tự.
//Khai báo biến str có kiểu String để lưu trữ một chuỗi ký tự.
//Gán giá trị cho các biến:
//Gán giá trị cho các biến khai báo ở trên (sử dụng các giá trị bất kỳ)
//In kết quả ra màn hình:
//Sử dụng lệnh System.out.println(“”) để in các giá trị của biến được khai báo ở trên ra màn hình.
public class Main {
    public static void main(String[] args) {
        int a = 10;
        double db = 10.5;
        char ch = 'A';
        String str = "Hello World!";
        System.out.println(a);
        System.out.println(db);
        System.out.println(ch);
        System.out.println(str);
    }
}